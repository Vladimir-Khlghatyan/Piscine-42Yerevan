int	ft_str_is_numeric(char *str)
{
	int	i;
	int	p;

	i = 0;
	if (*(str + i) == '\0')
		return (1);
	while (*(str + i) != '\0')
	{
		p = ((*(str + i) >= '0') && (*(str + i) <= '9'));
		if (!p)
			return (0);
		i++;
	}
	return (1);
}
