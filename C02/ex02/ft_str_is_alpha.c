int	ft_str_is_alpha(char	*str)
{
	int	i;

	if (!str || !str[0])
		return (1);
	i = -1;
	while (str[++i])
		if (!((str[i] >= 'A' && str[i] <= 'Z') || \
			(str[i] >= 'a' && str[i] <= 'z')))
			return (0);
	return (1);
}
