char	*ft_strlowcase(char	*str)
{
	int	i;
	int	p;

	i = 0;
	while (*(str + i))
	{
		p = (*(str + i) >= 'A' && *(str + i) <= 'Z');
		if (p)
			str[i] = str[i] + 32;
		i++;
	}
	return (str);
}
