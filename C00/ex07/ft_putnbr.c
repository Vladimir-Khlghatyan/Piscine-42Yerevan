#include <unistd.h>

void	ft_putnbr(int nb)
{
	unsigned int	x;
	char			c;

	x = nb;
	if (nb < 0)
	{
		write(1, "-", 1);
		x = -nb;
	}
	if (x > 9)
	{
		ft_putnbr(x / 10);
		ft_putnbr(x % 10);
	}
	else
	{
		c = x + '0';
		write(1, &c, 1);
	}
}
