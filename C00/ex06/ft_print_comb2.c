#include <unistd.h>

void	ft_print_num2(int a, int b, int c, int d)
{
	if ((a * 10 + b) < (c * 10 + d))
	{
		write(1, &a, 1);
		write(1, &b, 1);
		write(1, " ", 1);
		write(1, &c, 1);
		write(1, &d, 1);
		if (a < '9' || b < '8' || c < '9' || d < '9')
			write(1, ", ", 2);
	}
}

void	ft_print_comb2(void)
{
	int	n[4];

	n[0] = '0' - 1;
	n[1] = '0' - 1;
	n[2] = '0' - 1;
	n[3] = '1';
	while (++n[0] <= '9')
	{
		while (++n[1] <= '9')
		{
			while (++n[2] <= '9')
			{
				while (n[3] <= '9')
					ft_print_num2(n[0], n[1], n[2], n[3]++);
				n[3] = '0';
			}
			n[3] = '0';
			n[2] = '0' - 1;
		}
		n[3] = '0';
		n[2] = '0' - 1;
		n[1] = '0' - 1;
	}
}
